package heart;

public class SuitEvaluation {
	
	private int suit;
	private float value;
	
	public SuitEvaluation(int suit, float value) {
		this.suit = suit;
		this.value = value;
	}
	
	public int getSuit() {
		return this.suit;
	}
	
	public float getValue() {
		return this.value;
	}

}
